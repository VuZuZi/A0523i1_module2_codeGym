package ss1_IntroductionToJava.ThucHanh;

public class KhaiBaoVaSudungBien {
    public static void main(String[] args) {
        int a = 10;
        float f = 2.5f;
        double d = 20.5;
        boolean b = true;
        char c = 'a';
        String s = "Hà Nội";
        System.out.println("biến a kiểu số nguyên: "+a);
        System.out.println("biến f kiểu float: "+f);
        System.out.println("biến a kiểu double: "+d);
        System.out.println("biến a kiểu boolean: "+b);
        System.out.println("biến a kiểu kí tự: "+c);
        System.out.println("biến a kiểu chuỗi: "+s);
    }
}
